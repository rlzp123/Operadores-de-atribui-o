// 9️⃣ Conversão Implícita em Operadores Lógicos

console.log("5" - 2); // Não concatena (dados incompatíveis). O JavaScript converte "5" (string) para número automaticamente

console.log("5" + 2); // com string vira concatenação. Então o número 2 é convertido para string

console.log(true + 1); // Valores booleanos viram números: true → 1 false → 0

console.log(false + 10);// false vira 0